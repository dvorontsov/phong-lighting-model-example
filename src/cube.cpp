Cube::Cube(std::string vs, std::string fs) : shader(vs.c_str(), fs.c_str())
{
	// Generate buffers
	glGenBuffers(1, &vbo);
	glGenVertexArrays(1, &vao);

	// Bind
	glBindBuffer(GL_ARRAY_BUFFER, vbo);		

	// Copy data
	glBufferData(GL_ARRAY_BUFFER, sizeof(vertices), vertices, GL_STATIC_DRAW);

	glBindVertexArray(vao);

	// position attribute
	glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)0);
	glEnableVertexAttribArray(0);

	// normal attribute
	glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, 6 * sizeof(float), (void*)(3 * sizeof(float)));
	glEnableVertexAttribArray(1);

	// Unbind
	glBindBuffer(GL_ARRAY_BUFFER, 0);		

	shader.use();
}

void Cube::setModel(glm::mat4 mat)
{
	model = mat;
	shader.use();
	shader.setMat4("model", mat);
}

void Cube::setView(glm::mat4 mat)
{
	view = mat;
	shader.use();
	shader.setMat4("view", mat);
}

void Cube::setProjection(glm::mat4 mat)
{
	projection = mat;
	shader.use();
	shader.setMat4("projection", mat);
}


void Cube::render()
{
	shader.use();
	glBindVertexArray(vao);
	glDrawArrays(GL_TRIANGLES, 0, 36);
	glBindVertexArray(0);
}

Shader Cube::getShader()
{
	return this->shader;
}
